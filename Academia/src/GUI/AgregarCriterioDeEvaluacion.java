    private void aceptarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarButtonActionPerformed
        if (!validarCampos() || !validarFecha(fechaIn.getText())){
            JOptionPane.showMessageDialog(this, "Se deben llenar todos los campos o la fecha esta ingresada incorrectamente");
        }else{
            CriterioDeEvaluacion criterioDeEvaluacion = new CriterioDeEvaluacion();
            criterioDeEvaluacion.setUnidadesEvaluadas(unidadEvaluadaIn.getText());
            criterioDeEvaluacion.setFecha(Date.valueOf(fechaIn.getText()));
            criterioDeEvaluacion.setCriterioEvaluacion(criterioDeEvaluacionIn.getText());
            criterioDeEvaluacion.setInstrumento(instrumentosIn.getText());
            criterioDeEvaluacion.setPorcentaje(porcentajeIn.getText());
            ElaborarPlanDeCurso.criteriosDeEvaluacion.add(criterioDeEvaluacion);
            dispose();
       }
    }//GEN-LAST:event_aceptarButtonActionPerformed

    private void unidadEvaluadaInKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_unidadEvaluadaInKeyTyped
        char caracteres= evt.getKeyChar();
        if ((caracteres<'0'||caracteres>'9') && caracteres != '-') evt.consume();
        if(unidadEvaluadaIn.getText().length() >= 5){
            evt.consume();
        }
    }//GEN-LAST:event_unidadEvaluadaInKeyTyped

    private void fechaInKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fechaInKeyTyped
        char caracteres= evt.getKeyChar();
        if ((caracteres<'0'||caracteres>'9') && caracteres != '-') evt.consume();
        if(fechaIn.getText().length() >= 10){
            evt.consume();
        }
    }//GEN-LAST:event_fechaInKeyTyped

