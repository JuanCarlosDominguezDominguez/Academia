    public boolean validarCampos(){
        if(unidadEvaluadaIn.getText() == "" || fechaIn.getText() == "" || criterioDeEvaluacionIn.getText() == "" || instrumentosIn.getText() == "" || porcentajeIn.getText() == ""){
            return false;
        }
        return true;
    }
    
    public boolean validarFecha(String fecha){
        SimpleDateFormat estructura = new SimpleDateFormat("yyyy-mm-dd");
        estructura.setLenient(false);
        try{
            estructura.parse(fecha);
        }catch(ParseException excepcion){
            
            return false;
        }
        return true;
    }
